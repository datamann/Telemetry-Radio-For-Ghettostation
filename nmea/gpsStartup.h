uint8_t setBaudRate(uint32_t BAUDRATE)
{
  switch (BAUDRATE)
  {
      case 4800:
          {
          String txtToDisplay = "Setting baudrate to 4800: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                0xC0,0x12,0x00,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0xCF,0xE4};
          return baudRate;
          }
          break;
      case 9600:
          {
          String txtToDisplay = "Setting baudrate to 9600: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                0x80,0x25,0x00,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0xA2,0xB5};
          return baudRate;
          }
          break;
      case 19200:
          {
          String txtToDisplay = "Setting baudrate to 19200: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                 0x00,0x4B,0x00,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0x48,0x57};
          return baudRate;
          }
          break;
      case 38400:
          {
          String txtToDisplay = "Setting baudrate to 38400: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                 0x00,0x96,0x00,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0x93,0x90};
          return baudRate;
          }
          break;
      case 57600:
          {
          String txtToDisplay = "Setting baudrate to 57600: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                 0x00,0xE1,0x00,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0xDE,0xC9};
          return baudRate;
          }
          break;
      case 115200:
          {
          String txtToDisplay = "Setting baudrate to 115200: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                  0x00,0xC2,0x01,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0xC0,0x7E};
          return baudRate;
          }
          break;
      default:
          {
          String txtToDisplay = "Setting baudrate to 9600: ";
          uint8_t baudRate[] = {0xB5,0x62,0x06,0x00,0x14,0x00,0x01,0x00,0x00,0x00,0xD0,0x08,0x00,0x00,
                                0x80,0x25,0x00,0x00,0x07,0x00,0x03,0x00,0x00,0x00,0x00,0x00,0xA2,0xB5};
          return baudRate;
          }
  }
}
